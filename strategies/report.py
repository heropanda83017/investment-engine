def render_html(title, sections, report_date=""):
    parts = [f"<h1>{title}</h1>"]
    if report_date:
        parts.append(f'<p class="meta">{report_date}</p>')
    for sec in sections:
        parts.append(f"<h2>{sec.get('heading','')}</h2>")
        if sec.get("content"):
            parts.append(f"<div>{sec['content']}</div>")
        for img in sec.get("images", []):
            if img:
                parts.append(f'<img src="data:image/png;base64,{img}" />')
    parts.append(f'<div class="footer">Generated by investment-engine | {datetime.now().strftime("%Y-%m-%d %H:%M")}</div>')
    html = f"""<!DOCTYPE html><html><head><meta charset="utf-8"><title>{title}</title>{REPORT_CSS}</head><body>{"".join(parts)}</body></html>"""
    return html

def save_report(html, report_type, subdir="daily"):
    today = datetime.now()
    month_dir = Path(IE_ROOT) / "reports" / subdir / today.strftime("%Y-%m")
    month_dir.mkdir(parents=True, exist_ok=True)
    fpath = month_dir / f"{today.strftime('%Y-%m-%d')}-{report_type}.html"
    fpath.write_text(html, encoding="utf-8")
    _cleanup(month_dir, 30 if subdir == "daily" else 365)
    return str(fpath)

def _cleanup(directory, keep_days):
    cutoff = datetime.now() - timedelta(days=keep_days)
    for f in directory.glob("*.html"):
        try:
            t = datetime.fromtimestamp(f.stat().st_mtime)
            if t < cutoff:
                f.unlink()
        except: pass

def make_table(headers, rows):
    ths = "".join(f"<th>{h}</th>" for h in headers)
    trs = "".join(f"<tr>{''.join(f'<td>{c}</td>' for c in row)}</tr>" for row in rows)
    return f"<table><thead><tr>{ths}</tr></thead><tbody>{trs}</tbody></table>"

def make_metric_row(metrics):
    cards = ""
    for label, value, cls in metrics:
        cards += f'<div style="display:inline-block;min-width:120px;padding:8px 16px;margin:4px;background:#f8f9fa;border-radius:6px;text-align:center"><div style="font-size:11px;color:#888">{label}</div><div class="{cls}" style="font-size:18px">{value}</div></div>'
    return f'<div style="margin:12px 0">{cards}</div>'
