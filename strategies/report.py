def _fig_to_base64(fig):
    buf = io.BytesIO()
    fig.savefig(buf, format="png", dpi=100, bbox_inches="tight")
    buf.seek(0)
    img = base64.b64encode(buf.read()).decode("utf-8")
    plt.close(fig)
    return img

def plot_ic_bar(ic_data: pd.Series) -> str:
    if ic_data.empty:
        return ""
    fig, ax = plt.subplots(figsize=(10, 4))
    colors = ["#e74c3c" if v < 0 else "#2ecc71" for v in ic_data.values]
    ax.bar(ic_data.index, ic_data.values, color=colors, alpha=0.8)
    ax.axhline(y=0, color="gray", linestyle="--", linewidth=0.5)
    ax.set_title("Factor RankIC", fontsize=12)
    ax.set_ylabel("RankIC")
    fig.tight_layout()
    return _fig_to_base64(fig)

def plot_ic_trend(ic_history: pd.DataFrame) -> str:
    if ic_history.empty:
        return ""
    fig, ax = plt.subplots(figsize=(12, 5))
    for col in ic_history.columns:
        ax.plot(ic_history.index, ic_history[col], label=col, alpha=0.7)
    ax.axhline(y=0, color="gray", linestyle="--", linewidth=0.5)
    ax.legend(loc="best", fontsize=8)
    ax.set_title("Factor IC Trend (20d)", fontsize=12)
    fig.tight_layout()
    return _fig_to_base64(fig)

def plot_pnl_curve(pnl_series: pd.Series) -> str:
    if pnl_series.empty:
        return ""
    fig, ax = plt.subplots(figsize=(12, 5))
    ax.fill_between(pnl_series.index, pnl_series.values, alpha=0.3, color="#3498db")
    ax.plot(pnl_series.index, pnl_series.values, color="#2980b9", linewidth=1.5)
    ax.set_title("Strategy NAV Curve", fontsize=12)
    ax.set_ylabel("NAV")
    ax.yaxis.set_major_formatter(mticker.FormatStrFormatter("%.4f"))
    fig.tight_layout()
    return _fig_to_base64(fig)

